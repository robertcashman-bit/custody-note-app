function flattenPrivacyNoticeSections(sections) {
  return (sections || []).map(function (sec) {
    var parts = [];
    if (sec.heading) parts.push(sec.heading);
    (sec.paragraphs || []).forEach(function (p) { parts.push(p); });
    (sec.bullets || []).forEach(function (b) { parts.push('\u2022 ' + b); });
    return parts.join('\n\n');
  }).join('\n\n');
}

